#!/usr/bin/env node
/**
 * audit-suite.js — Self-audit for claude-tracker-suite
 *
 * Usage: node audit-suite.js [--json] [--output <path>]
 *
 * Checks: script inventory (shebangs, dead paths, unreferenced scripts),
 * shell portability (BSD/GNU divergent flags), daemon/launchd health,
 * DB coverage vs on-disk transcripts, and a search recall self-test.
 * Writes AUDIT.md (P0-P3 findings) to the skill directory by default.
 */

const os = require('os');
const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');
const utils = require(path.join(os.homedir(), '.claude', 'lib', 'tracker-utils.js'));

const HOME = os.homedir();
const SKILL_DIR = path.join(HOME, '.claude', 'skills', 'claude-tracker-suite');
const SCRIPTS_DIR = path.join(SKILL_DIR, 'scripts');
const LIB_DIR = path.join(HOME, '.claude', 'lib');
const PROJECTS_DIR = path.join(HOME, '.claude', 'projects');

const args = process.argv.slice(2);
const jsonMode = args.includes('--json');
const outIdx = args.indexOf('--output');
const outputPath = outIdx !== -1 && args[outIdx + 1]
  ? args[outIdx + 1]
  : path.join(SKILL_DIR, 'AUDIT.md');

// P0 = broken now; P1 = silently wrong results; P2 = fragile/incomplete; P3 = hygiene
const findings = [];
function finding(priority, area, title, detail) {
  findings.push({ priority, area, title, detail });
}

function sh(cmd) {
  try {
    return execSync(cmd, { encoding: 'utf8', timeout: 10000 }).trim();
  } catch { return ''; }
}

// ---------------------------------------------------------------------------
// 1. Script inventory
// ---------------------------------------------------------------------------

function auditInventory() {
  const inventory = [];
  const scriptFiles = fs.readdirSync(SCRIPTS_DIR)
    .filter(f => /\.(js|sh|py)$/.test(f) || !f.includes('.'));

  // Corpus of text that may reference scripts (SKILL.md, commands, hooks, settings)
  const refSources = [];
  const addRefFile = (p) => { try { refSources.push(fs.readFileSync(p, 'utf8')); } catch {} };
  addRefFile(path.join(SKILL_DIR, 'SKILL.md'));
  addRefFile(path.join(HOME, '.claude', 'settings.json'));
  for (const dir of [path.join(HOME, '.claude', 'commands'), path.join(HOME, '.claude', 'hooks'), SCRIPTS_DIR, path.join(SKILL_DIR, 'references')]) {
    try {
      for (const f of fs.readdirSync(dir)) {
        if (/\.(md|sh|js|py|json)$/.test(f)) addRefFile(path.join(dir, f));
      }
    } catch {}
  }
  const refCorpus = refSources.join('\n');

  for (const f of scriptFiles) {
    const fp = path.join(SCRIPTS_DIR, f);
    const stat = fs.statSync(fp);
    if (!stat.isFile()) continue;
    const content = fs.readFileSync(fp, 'utf8');
    const firstLine = content.split('\n')[0];
    const entry = { file: f, size: stat.size, issues: [] };

    // Shebang check
    if (/\.(js|sh|py)$/.test(f) || (stat.mode & 0o111)) {
      if (!firstLine.startsWith('#!')) {
        entry.issues.push('missing shebang');
      } else {
        const interp = firstLine.replace('#!', '').trim().split(/\s+/);
        const bin = interp[0] === '/usr/bin/env' ? sh(`which ${interp[1]}`) : interp[0];
        if (!bin || !fs.existsSync(bin.split(/\s/)[0])) {
          entry.issues.push(`shebang interpreter not found: ${firstLine}`);
        }
      }
    }
    // Executable bit for shell scripts
    if (f.endsWith('.sh') && !(stat.mode & 0o111)) {
      entry.issues.push('shell script not executable');
    }
    // Referenced absolute ~/.claude paths that don't exist
    const pathRefs = content.match(/(?:~|\$HOME|os\.homedir\(\)|process\.env\.HOME)[^'"`\s)]*\/\.claude\/[A-Za-z0-9._\/-]+/g) || [];
    const checkedPaths = new Set();
    // Files created at runtime or generated by the script itself (bootstrap
    // templates) — absent until first use, not broken references
    const RUNTIME_ARTIFACTS = /^(workspace-state\.json|session-registry\.json|session-summaries\.json|tracker\.db|git-tracking-index\.json|bookmarks|userModels\/userModel\.md)/;
    for (const raw of pathRefs) {
      const rel = raw.replace(/^.*\/\.claude\//, '');
      // Skip template fragments and runtime-generated paths
      if (/[${}<>*@]|\.db-|\.jsonl$/.test(rel)) continue;
      if (RUNTIME_ARTIFACTS.test(rel)) continue;
      const abs = path.join(HOME, '.claude', rel);
      if (checkedPaths.has(abs)) continue;
      checkedPaths.add(abs);
      if (!fs.existsSync(abs)) {
        entry.issues.push(`references missing path: ~/.claude/${rel}`);
      }
    }
    // Unreferenced: name appears nowhere in docs/commands/hooks/other scripts
    const selfless = refCorpus.split(f).length - (content.includes(f) ? 2 : 1);
    if (selfless < 1) entry.unreferenced = true;

    inventory.push(entry);
  }

  for (const e of inventory) {
    for (const issue of e.issues) {
      const pri = issue.startsWith('references missing') ? 'P1' : 'P2';
      finding(pri, 'inventory', `${e.file}: ${issue}`, `scripts/${e.file}`);
    }
    if (e.unreferenced) {
      finding('P3', 'inventory', `${e.file} is referenced nowhere`, 'Not mentioned in SKILL.md, commands, hooks, or other scripts — dead code candidate.');
    }
  }
  return inventory;
}

// ---------------------------------------------------------------------------
// 2. Shell portability (BSD vs GNU flag divergence on macOS)
// ---------------------------------------------------------------------------

const PORTABILITY_PATTERNS = [
  { re: /\bgrep\b[^|\n]*\s-[a-zA-Z]*P/, why: 'grep -P (PCRE) is GNU-only; BSD grep lacks it' },
  { re: /\bgrep\b[^|\n]*\s-[a-zA-Z]*Z/, why: 'grep -Z means zgrep-decompress on BSD, NUL-terminate on GNU' },
  { re: /\bsed\s+-i\s+(?!'')(?!"")[^ ']/, why: "sed -i without '' backup arg breaks on BSD sed" },
  { re: /\bdate\s+(-[a-zA-Z]*\s+)*-d\s/, why: 'date -d is GNU-only; BSD date uses -v/-j -f' },
  { re: /\bstat\s+(-[a-zA-Z]*\s+)*-c\s/, why: 'stat -c is GNU-only; BSD stat uses -f' },
  { re: /\breadlink\s+-f\b/, why: 'readlink -f is GNU-only on older macOS (ok on macOS 12.3+)' },
];

function auditPortability() {
  const hits = [];
  const scanDirs = [SCRIPTS_DIR, path.join(HOME, '.claude', 'scripts')];
  for (const dir of scanDirs) {
    let files = [];
    try { files = fs.readdirSync(dir).filter(f => /\.(sh|js|py)$/.test(f)); } catch { continue; }
    for (const f of files) {
      if (f === 'audit-suite.js') continue; // don't flag our own pattern definitions
      const fp = path.join(dir, f);
      let lines;
      try { lines = fs.readFileSync(fp, 'utf8').split('\n'); } catch { continue; }
      lines.forEach((line, i) => {
        if (/^\s*(#|\/\/|\*)/.test(line)) return; // skip comments
        for (const { re, why } of PORTABILITY_PATTERNS) {
          if (re.test(line)) {
            hits.push({ file: path.relative(HOME, fp), line: i + 1, why, snippet: line.trim().slice(0, 100) });
          }
        }
      });
    }
  }
  for (const h of hits) {
    finding('P2', 'portability', `${h.file}:${h.line} — ${h.why}`, h.snippet);
  }
  return hits;
}

// ---------------------------------------------------------------------------
// 3. Daemon / launchd health
// ---------------------------------------------------------------------------

function auditDaemons() {
  const health = {};

  health.watcherRunning = !!sh('pgrep -f claude-tracker-watch');
  if (!health.watcherRunning) {
    finding('P2', 'daemons', 'claude-tracker-watch daemon not running',
      'New sessions will not be auto-summarized or indexed. Start: claude-tracker-watch --daemon');
  }

  const launchdEntry = sh('launchctl list 2>/dev/null | grep workspace-snapshot');
  health.snapshotLaunchdLoaded = !!launchdEntry;
  const statePath = path.join(HOME, '.claude', 'workspace-state.json');
  health.workspaceStateExists = fs.existsSync(statePath);
  if (health.workspaceStateExists) {
    const ageMin = (Date.now() - fs.statSync(statePath).mtimeMs) / 60000;
    health.workspaceStateAgeMinutes = Math.round(ageMin);
  }
  if (!health.snapshotLaunchdLoaded) {
    finding('P1', 'daemons', 'workspace-snapshot launchd job not loaded',
      'Restart-resume depends on periodic snapshots. Install: cp scripts/com.claude.workspace-snapshot.plist ~/Library/LaunchAgents/ && launchctl load ...');
  } else if (health.workspaceStateExists && health.workspaceStateAgeMinutes > 30) {
    finding('P2', 'daemons', `workspace-state.json is stale (${health.workspaceStateAgeMinutes} min old)`,
      'launchd job is loaded but snapshots are not landing every 300s as configured.');
  }
  if (!health.workspaceStateExists) {
    finding('P1', 'daemons', 'workspace-state.json does not exist',
      'No snapshot to restore from after a restart. Run save-workspace.js once, and fix the launchd job.');
  }

  const speculatorStatus = sh('bash ' + path.join(HOME, '.claude', 'scripts', 'speculator', 'status.sh') + ' 2>/dev/null | head -3');
  health.speculator = speculatorStatus || 'status script unavailable';

  return health;
}

// ---------------------------------------------------------------------------
// 4. DB coverage vs disk
// ---------------------------------------------------------------------------

function auditCoverage() {
  const cov = {};
  const db = utils.tryDb();

  // Top-level session transcripts (what getAllSessionFiles sees)
  const mainFiles = utils.getAllSessionFiles();
  cov.mainTranscripts = mainFiles.length;

  // Nested JSONL (tool-results payloads etc.) — NOT sessions, must stay excluded
  const nested = sh(`find "${PROJECTS_DIR}" -mindepth 3 -name "*.jsonl" 2>/dev/null | wc -l`);
  cov.nestedJsonl = parseInt(nested, 10) || 0;

  if (!db) {
    finding('P0', 'coverage', 'tracker.db unavailable', 'All SQLite paths are dead; only file-scan search works.');
    return cov;
  }

  const d = db.ensureTranscripts(); // transcript tables live in the sidecar DB
  cov.dbSessions = d.prepare('SELECT COUNT(*) c FROM sessions').get().c;
  cov.withSummary = d.prepare("SELECT COUNT(*) c FROM sessions WHERE summary IS NOT NULL AND summary != ''").get().c;
  cov.withAutoTitle = d.prepare("SELECT COUNT(*) c FROM sessions WHERE auto_title IS NOT NULL AND auto_title != ''").get().c;
  cov.withFirstPrompt = d.prepare("SELECT COUNT(*) c FROM sessions WHERE first_prompt IS NOT NULL AND first_prompt != ''").get().c;

  cov.encodedProjectPaths = d.prepare("SELECT COUNT(*) c FROM sessions WHERE project_path NOT LIKE '/%'").get().c;
  if (cov.encodedProjectPaths > 0) {
    finding('P1', 'coverage', `${cov.encodedProjectPaths} sessions store the encoded dir name as project_path`,
      'Resume commands print broken cd paths. Writer regression in session-sync-db.py decode_project_dir — repair with the decoder and fix the writer.');
  }

  const dbIds = new Set(d.prepare('SELECT session_id FROM sessions').all().map(r => r.session_id));
  const unmigrated = mainFiles.filter(f => !dbIds.has(path.basename(f.filePath, '.jsonl')));
  cov.unmigrated = unmigrated.length;
  cov.unmigratedSample = unmigrated.slice(0, 10).map(f => path.basename(f.filePath, '.jsonl'));

  const summaryPct = Math.round(100 * cov.withSummary / cov.dbSessions);
  if (summaryPct < 95) {
    finding('P1', 'coverage', `Summary coverage ${summaryPct}% (${cov.withSummary}/${cov.dbSessions})`,
      'Sessions without summaries are invisible to metadata-weighted search ranking. ' +
      'Summarizer is disabled by default (2026-07-13); when re-enabled: backfill-summaries.js --enable --provider openrouter.');
  }
  if (cov.unmigrated > 0) {
    finding('P2', 'coverage', `${cov.unmigrated} on-disk transcripts missing from tracker.db`,
      'Run migrate-to-sqlite.js --incremental. Sample: ' + cov.unmigratedSample.slice(0, 3).join(', '));
  }

  // Transcript FTS coverage (post-Phase-1 this should match mainTranscripts)
  try {
    cov.transcriptFtsSessions = d.prepare('SELECT COUNT(DISTINCT session_id) c FROM transcript_messages').get().c;
  } catch {
    cov.transcriptFtsSessions = 0;
    finding('P1', 'coverage', 'No transcript full-text index exists',
      'Body search requires a sequential 10 GB JSONL scan. Build via index-transcripts.js.');
  }

  return cov;
}

// ---------------------------------------------------------------------------
// 5. Search recall self-test
// ---------------------------------------------------------------------------

// Known-content fixtures: query → session that must appear.
// The first entry is the 2026-07-13 live failure that motivated this audit.
const SEARCH_FIXTURES = [
  { query: 'twitter background subquadratic imagemagick', expect: '0a13a63b-8402-488e-94e0-48dc6a4821a7', kind: 'transcript recall (vocabulary mismatch)' },
  { query: 'subquadratic porthole symbol', expect: '0a13a63b-8402-488e-94e0-48dc6a4821a7', kind: 'transcript recall (exact vocabulary)' },
  { query: 'image editing', expect: '0a13a63b-8402-488e-94e0-48dc6a4821a7', kind: 'metadata recall (auto title)' },
];

function auditSearch() {
  const db = utils.tryDb();
  const results = [];
  for (const fx of SEARCH_FIXTURES) {
    const r = { ...fx, metadataHit: false, transcriptHit: false };
    if (db) {
      try {
        const meta = db.searchSessions(fx.query, { limit: 50 });
        r.metadataHit = meta.some(s => s.session_id === fx.expect);
      } catch {}
      try {
        if (db.searchTranscripts) {
          const body = db.searchTranscripts(fx.query, { limit: 50 });
          r.transcriptHit = body.some(s => s.session_id === fx.expect);
        }
      } catch {}
    }
    r.pass = r.metadataHit || r.transcriptHit;
    if (!r.pass) {
      finding('P1', 'search', `Recall miss: "${fx.query}" does not surface ${fx.expect.slice(0, 8)}`, fx.kind);
    }
    results.push(r);
  }
  return results;
}

// ---------------------------------------------------------------------------
// Report
// ---------------------------------------------------------------------------

function writeReport({ inventory, portability, daemons, coverage, search }) {
  const now = new Date().toISOString().slice(0, 16).replace('T', ' ');
  const byPri = { P0: [], P1: [], P2: [], P3: [] };
  for (const f of findings) byPri[f.priority].push(f);

  let md = `# claude-tracker-suite Audit\n\nGenerated ${now} by \`scripts/audit-suite.js\`.\n\n`;
  md += `## Summary\n\n| Priority | Count |\n|----------|-------|\n`;
  for (const p of ['P0', 'P1', 'P2', 'P3']) md += `| ${p} | ${byPri[p].length} |\n`;

  md += `\n## Findings\n\n`;
  for (const p of ['P0', 'P1', 'P2', 'P3']) {
    if (byPri[p].length === 0) continue;
    md += `### ${p}\n\n`;
    for (const f of byPri[p]) {
      md += `- **[${f.area}]** ${f.title}\n  ${f.detail}\n`;
    }
    md += '\n';
  }

  md += `## Data\n\n### Coverage\n\n`;
  md += `| Metric | Value |\n|--------|-------|\n`;
  md += `| Main session transcripts on disk | ${coverage.mainTranscripts} |\n`;
  md += `| Nested JSONL (tool-results, excluded) | ${coverage.nestedJsonl} |\n`;
  md += `| Sessions in tracker.db | ${coverage.dbSessions ?? 'n/a'} |\n`;
  md += `| Unmigrated transcripts | ${coverage.unmigrated ?? 'n/a'} |\n`;
  md += `| With summary | ${coverage.withSummary ?? 'n/a'} |\n`;
  md += `| With auto_title | ${coverage.withAutoTitle ?? 'n/a'} |\n`;
  md += `| With first_prompt | ${coverage.withFirstPrompt ?? 'n/a'} |\n`;
  md += `| Sessions in transcript FTS | ${coverage.transcriptFtsSessions ?? 0} |\n`;

  md += `\n### Daemons\n\n`;
  md += `- Watcher running: ${daemons.watcherRunning}\n`;
  md += `- Snapshot launchd loaded: ${daemons.snapshotLaunchdLoaded}\n`;
  md += `- workspace-state.json: ${daemons.workspaceStateExists ? `present (${daemons.workspaceStateAgeMinutes} min old)` : 'MISSING'}\n`;

  md += `\n### Search self-test\n\n| Query | Kind | Metadata | Transcript | Pass |\n|-------|------|----------|------------|------|\n`;
  for (const r of search) {
    md += `| ${r.query} | ${r.kind} | ${r.metadataHit ? '✓' : '✗'} | ${r.transcriptHit ? '✓' : '✗'} | ${r.pass ? '✅' : '❌'} |\n`;
  }

  md += `\n### Inventory (${inventory.length} scripts)\n\n`;
  const clean = inventory.filter(e => e.issues.length === 0 && !e.unreferenced).length;
  md += `${clean} clean, ${inventory.length - clean} with findings (detailed above).\n`;

  fs.writeFileSync(outputPath, md);
  return md;
}

function main() {
  const inventory = auditInventory();
  const portability = auditPortability();
  const daemons = auditDaemons();
  const coverage = auditCoverage();
  const search = auditSearch();

  const report = { inventory, portability, daemons, coverage, search, findings };

  if (jsonMode) {
    console.log(JSON.stringify(report, null, 2));
  } else {
    writeReport(report);
    const counts = ['P0', 'P1', 'P2', 'P3']
      .map(p => `${p}:${findings.filter(f => f.priority === p).length}`)
      .join(' ');
    console.log(`Audit complete — ${findings.length} findings (${counts})`);
    console.log(`Report: ${outputPath}`);
  }
}

main();
